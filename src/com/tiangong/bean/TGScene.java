package com.tiangong.bean;

public class TGScene {

    private int roomid;
    private int mysceneid;
    private int sceneaddr;
    private String scenename;
    public int getRoomid() {
        return roomid;
    }
    public void setRoomid(int roomid) {
        this.roomid = roomid;
    }
    public int getMysceneid() {
        return mysceneid;
    }
    public void setMysceneid(int mysceneid) {
        this.mysceneid = mysceneid;
    }
    public int getSceneaddr() {
        return sceneaddr;
    }
    public void setSceneaddr(int sceneaddr) {
        this.sceneaddr = sceneaddr;
    }
    public String getScenename() {
        return scenename;
    }
    public void setScenename(String scenename) {
        this.scenename = scenename;
    }
    
}
